# Create a list of favourite fruits
favourite_fruits = ["Orange", "Apple", "Grapes", "Mango", "Banana"]

# Print the first fruit using index 0
print(favourite_fruits[0])

# Print the last fruit using negative indexing
print(favourite_fruits[-1])

# Add a new fruit to the list
favourite_fruits.append("Strawberry")

# Print the updated list
print(favourite_fruits)